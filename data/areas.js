export const cities = [...detailedCities, ...autoCities];

export function getRegion(slug) {
  return regions.find((entry) => entry.slug === slug) || null;
}

export function getPrefecture(slug) {
  return prefectures.find((entry) => entry.slug === slug) || null;
}

export function getPrefecturesByRegion(slug) {
  return prefectures.filter((entry) => entry.region === slug);
}

export function getCitiesByPrefecture(slug) {
  return cities.filter((entry) => entry.prefecture === slug);
}

export function getCity(prefectureSlug, citySlug) {
  return cities.find((entry) => entry.prefecture === prefectureSlug && entry.slug === citySlug) || null;
}

export function getPriorityCityObjects(prefectureSlug) {
  const prefecture = getPrefecture(prefectureSlug);
  if (!prefecture) return [];
  return prefecture.priorityCities
    .map((slug) => getCity(prefectureSlug, slug))
    .filter(Boolean);
}

const cityClusterLayouts = {
  3: [
    { x: 22, y: 28 },
    { x: 58, y: 18 },
    { x: 70, y: 62 }
  ],
  6: [
    { x: 18, y: 24 },
    { x: 44, y: 14 },
    { x: 72, y: 22 },
    { x: 24, y: 62 },
    { x: 50, y: 52 },
    { x: 76, y: 68 }
  ]
};

export function getCityCluster(prefectureSlug) {
  const items = getPriorityCityObjects(prefectureSlug);
  const layout = cityClusterLayouts[items.length] || cityClusterLayouts[6];
  return items.map((city, index) => ({
    ...city,
    position: layout[index] || { x: 18 + index * 10, y: 20 + index * 8 }
  }));
}

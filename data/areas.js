function normalizeReaderText(text) {
  let out = text;
  for (const [from, to] of readerReplacements) {
    out = out.replaceAll(from, to);
  }

  return out
    .replace(/売却判断を作りやすい/g, '売り方を整理しやすい')
    .replace(/説明を作りやすい/g, '整理して考えやすい')
    .replace(/県内ハブとして使いやすい/g, '県内の中心として見られやすい')
    .replace(/商業意図/g, '売却を急ぐ相談')
    .replace(/テーマ設計/g, '地域の違い')
    .replace(/地場ページの薄さも残っています/g, '都市ごとの差を見ながら売り方を考えやすいです')
    .replace(/刺さりやすい/g, '重要になりやすい')
    .replace(/効きやすい/g, '大切になりやすい')
    .replace(/狙いやすい/g, '相談しやすい')
    .replace(/([^\s。]+)の地域差を踏まえた比較がしやすい/g, '$1では売り方の比較が欠かせない')
    .replace(/拾いやすい/g, '整理しやすい');
}

function normalizeReaderList(list) {
  return list.map((item) => normalizeReaderText(item));
}

